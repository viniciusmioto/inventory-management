    public void read(){
        DefaultTableModel model = (DefaultTableModel) jTableProd.getModel();
        ProductDAO pdao = new ProductDAO();
        model.setNumRows(0);
        pdao.read().forEach((p) -> {
            model.addRow(new Object[]{
                p.getId(),
                p.getDescription(),
                p.getPrice(),
                p.getQuantity()
            });
        });
    }
    
    public void search(String description){
        DefaultTableModel model = (DefaultTableModel) jTableProd.getModel();
        ProductDAO pdao = new ProductDAO();
        model.setNumRows(0);
        pdao.search(description).forEach((p) -> {
            model.addRow(new Object[]{
                p.getId(),
                p.getDescription(),
                p.getQuantity(),
                p.getPrice()
            });
        });
    }

