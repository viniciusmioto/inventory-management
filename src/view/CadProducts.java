    private void btnDeleteProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteProdActionPerformed
        if(jTableProd.getSelectedRow() != -1){
            Products prod = new Products();
            ProductDAO pdao = new ProductDAO();
            
            prod.setId((int) jTableProd.getValueAt(jTableProd.getSelectedRow(), 0));
            pdao.delete(prod);
            
        }else{
            JOptionPane.showMessageDialog(null, "Select an item to delete");
        }
        jProdName.setText(null);
        jProdPrice.setText(null);
        spnQuantity.setValue(1);
        read();
    }//GEN-LAST:event_btnDeleteProdActionPerformed

    private void jTableProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdMouseClicked
        if(jTableProd.getSelectedRow() != -1){
            jProdName.setText(jTableProd.getValueAt(jTableProd.getSelectedRow(), 1).toString());
            jProdPrice.setText(jTableProd.getValueAt(jTableProd.getSelectedRow(), 2).toString());
            spnQuantity.setValue(jTableProd.getValueAt(jTableProd.getSelectedRow(), 3));
        } else{
            JOptionPane.showMessageDialog(null, "Select an item to Update.");
        }
    }//GEN-LAST:event_jTableProdMouseClicked

    private void btnUpdateProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProdActionPerformed
        if(jTableProd.getSelectedRow() != -1){
            Products prod = new Products();
            ProductDAO pdao = new ProductDAO();
            
            prod.setId((int) jTableProd.getValueAt(jTableProd.getSelectedRow(), 0));
            prod.setDescription(jProdName.getText());
            prod.setQuantity((int) spnQuantity.getValue());
            prod.setPrice(Double.parseDouble(jProdPrice.getText()));
            
            pdao.update(prod);
            
        } else{
            JOptionPane.showMessageDialog(null, "Select an item to Update.");
        } 
        
        jProdName.setText(null);
        jProdPrice.setText(null);
        spnQuantity.setValue(1);
        read();
    }//GEN-LAST:event_btnUpdateProdActionPerformed

