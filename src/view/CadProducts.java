    private void btnProdSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdSaveActionPerformed
        Products prod = new Products();
        ProductDAO pdao = new ProductDAO();
        prod.setDescription(jProdName.getText());
        prod.setQuantity((int) spnQuantity.getValue());
        prod.setPrice(Double.parseDouble(jProdPrice.getText()));
        pdao.create(prod);
        jProdName.setText(null);
        jProdPrice.setText(null);
        spnQuantity.setValue(1);
        read();
        
    }//GEN-LAST:event_btnProdSaveActionPerformed

